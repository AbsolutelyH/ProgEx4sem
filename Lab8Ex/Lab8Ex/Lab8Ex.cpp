void reg()                                                           //Изменение регистра
{
	char s;                                                          //Вводимая буква
	cout << "Введите латинскую букву в нижнем регистре:" << endl;
	cin >> s;
	s -= 32;                                                         //Изменение интового значения буквы по таблице ASCII
	cout << "Буква в верхнем регистре: " << s << endl << endl;               //Вывод
}

