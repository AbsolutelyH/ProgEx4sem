#include <iostream>
#include  <ctime>

using namespace std;

void fNum()
{
	int num;
	int n[5];                                                   // Массив для хранения чисел 
	cout << "Введите пятизначное число" << endl;                // Ввод пятизначного числа
	cin >> num;
	cout << endl;
	for (int i = 0; i < 5; i++)                                 // Заполение массива путем деления числна на десять без остатка 
	{
		n[i] = num % 10;
    	num = num / 10;
	}

	for (int i = 4; i > -1; i--)								// Вывод цыфр числа построчно
	{
		cout << n[i] << endl;
		
	}
	cout << endl;
}

