void arithMean()                                                             // Подсчёт среденго арифметического массива
{
	const int size = 10;                                                     // Размер массива
	int s = 0;                                                               // Переменная для подсчёта суммы элементов массива
		int array[size];                                                     // Массив 
	for (int i = 0; i < size; i++)                                           // Заполнение массива случайными числами от 0 до 100
		array[i] = rand() % 100;
	cout << "Массив:" << endl;                                               // Вывод массива
	for (int i = 0; i < size; i++)
		cout << array[i] << ' ';
	cout << endl;
	for (int i = 0; i < size; i++)                                           //Подсчёт суммы всех элементов массива
		s += array[i];
	cout << "Среднее арифметическое массива: " << double(s) / size << endl;  // Вывод среднего арифметического
}

