void square()                                                                         // решение квадратного уравнения с коэффициентами a = 5, b = 11, c = 2
{
    double a = 5, b = 11, c = 2;                                                         // коэффициенты 
    double x1, x2;                                                                    // корни
    int d = b * b - 4 * a * c;                                                        // дискриминант
    if (d > 0)                                                                        // когда дискриминант положительный
    {
        x1 = ((-b) - sqrt(d)) / (2 * a);
        x2 = ((-b) + sqrt(d)) / (2 * a);
        cout << "Первый корень: " << x1 << endl << "Второй корень: " << x2 << endl;
    }
    if (d == 0)                                                                       // когда дискриминант = 0
    {
        x1 = (-b) / (2 * a);
        cout << "Корень: " << x1 << endl;
    }
    if (d < 0)                                                                        // когда дискриминант меньше нуля
    {
        cout << "Корней нет" << endl;
    }
}

