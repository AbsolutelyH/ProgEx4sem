void puz()                                          // сортировка пузырьком
{
    int arr[10];                                    // массив
    int temp;                                       // вспомогательная перменная для сортировки
    for (int i = 0; i < 10; i++)                    // заполнение массива случайными числами от 0 до 100
        arr[i] = rand() % 100;;
    for (int i = 0; i < 10; i++)                    // вывод массива
        cout << arr[i] << ' ';
        cout << endl;
        cout << endl;
    for (int i = 0; i < 10 - 1; i++)                // сортировка
    {
        for (int j = 0; j < 10 - i - 1; j++)
        {
            if (arr[j] > arr[j + 1])
            {
                temp = arr[j];
                arr[j] = arr[j + 1];
                arr[j + 1] = temp;
            }
        }
    }
    for (int i = 0; i < 10; i++)                    // вывод массива
        cout << arr[i] << ' ';
    cout << endl;
    cout << endl;
}

